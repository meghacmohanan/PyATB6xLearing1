
# Tuple in python
# Collection of items - ordered and immutable (unchangeable)
# Allows duplicates

my_tuple= (1,2,3,4,5)
print(my_tuple)
print(my_tuple[0])
# with mixed data types

info= ("Megha", 34, True, 3.14)
